namespace SpaceDefence
{
    class HUD
    {
        private SpriteFont font; 
        private Ship playerShip; 
        private Vector2 position; 

        public HUD(Ship ship)
        {
            playerShip = ship;
            position = new Vector2(10, 10); 
        }

        public void Load(ContentManager content)
        {
            font = content.Load<SpriteFont>("Font"); 
        }

        public void Draw(SpriteBatch spriteBatch)
        {
            string cargoStatusText = "Cargo: " + (playerShip.isCarryingCargo ? "Yes" : "No");
            string scoreText = "Points: " + playerShip.cargoPoints.ToString();

            spriteBatch.DrawString(font, cargoStatusText, position, Color.White);
            spriteBatch.DrawString(font, scoreText, new Vector2(position.X, position.Y + 30), Color.White);
        }
    }
}
